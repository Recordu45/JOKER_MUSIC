NORMAL_UPPER = list("ABCDEFGHIJKLMNOPQRSTUVWXYZ")
NORMAL_LOWER = list("abcdefghijklmnopqrstuvwxyz")

async def CHAT_TITLE(text: str) -> str:
    """
    Convert fancy fonts back to normal text.
    Works for all font variations in the list.
    """
    out = text

    # Convert uppercase first
    for font in UPPER_FONTS:
        for f, n in zip(font, NORMAL_UPPER):
            out = out.replace(f, n)

    # Convert lowercase
    for font in LOWER_FONTS:
        for f, n in zip(font, NORMAL_LOWER):
            out = out.replace(f, n)

    return out
